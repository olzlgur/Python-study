#총 10자리를 확보하고 오른쪽 정렬을 한 뒤 출력 
print("{0:>10}" .format(10))

#부호를 함께 출력
print("{0: >+10}" .format(500))

#빈 공간을 '_'로 채움
print("{0:_<10}" .format(50))

#3자리마다 ',' 찍어주기
print("{0:,}" .format(100000000))

print("{0:^>+30,}" .format(10000000000))

#소수점 출력
print("{0:f}".format(5/3))

#소수점 특정 자리수까지 출력
print("{0:.2f}".format(5/3))